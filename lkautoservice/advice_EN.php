    <p class="advh">How to change sparkplugs</p>
<article class="row">
    <ol class="col-lg-9">
        <li>Firstly you need to locate them. The easiest way to do that is to find your car’s manual, which has detailed description of the position of components. The general rule of thumb, however is that on any inline engine (All of the cylinders are in one horizontal line) the spark plugs are either on the top or side of the engine, positioned in a single row, and on V-shaped engines (There is more than 1 row of cylinders, in a V-shape) they should be evenly separated on each part of the engine.</li>
        <li>WARNING: Let the car cool off before you touch anything on the engine</li>
        <li>Pull the wire plug out by hand. DO NOT pull hard or pull by the wire, clutch the plug itself and pull it out, it should not require a lot of force to pull out.</li>
        <li>Use your socket wrench to take the spark plug out, make sure the wrench is at the very bottom of the opening and turn clock-wise. The spark plug should come out.</li>
        <li>Use this opportunity to clean around the wire connections with compressed air, in order to insure a snug fit.</li>
        <li>Place your replacement plugs into the socket wrench and put the new plug in.</li>
    </ol>
