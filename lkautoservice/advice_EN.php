    <p class="advh">How to change oil</p>
<article class="row">
    <ol class="col-lg-9">
        <li>Park your car somewhere on level ground. Put the car in 1st gear or in Park if you’re driving an automatic.</li>
        <li>Put an object, like a bit of wood or specialized wheel stops behind your rear tyres. This is done to avoid the car moving backwards.</li>
        <li>You need to find your car’s jacking points. These are safe places where jacks can be placed in order to lift up the car.</li>
        <li>Jack up the front part of the car, so that both the front tyres are off the ground</li>
        <li>Open up your oil cap</li>
        <li>Look for your oil pan under the car. It’s usually located right below the oil cap, and is easily distinguishable by a single nut on the sides of it.</li>
        <li>After you’ve located the oil pan, place a container of some sort below it, you don’t want contaminated oil spilling on the ground</li>
        <li>Unscrew the nut on the oil pan, wait until all of the oil has drained out.</li>
        <li>Don’t forget to screw the nut back on.</li>
        <li>Put in the new oil. As the engine parts are already coated with a previous layer of oil, you can use a leaner oil consistency, in order to not strain the engine with more lubricant than is required.</li>

    </ol>
